    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        try {
        double monto = Double.parseDouble(txtMonto.getText());
        model.CuentaBancaria cuentaActiva = obtenerCuentaSeleccionada();
        
        if (cuentaActiva != null) {
            // La vista delega la operación al controlador
            boolean exito = controller.depositar(cuentaActiva, monto);
            
            if (exito) {
                javax.swing.JOptionPane.showMessageDialog(this, "Depósito procesado con éxito.");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar en Base de Datos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
            txtMonto.setText(""); 
            actualizarPantalla(); 
        }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese un monto numérico válido.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnDepositarActionPerformed

    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        try {
        double monto = Double.parseDouble(txtMonto.getText());
        model.CuentaBancaria cuentaActiva = obtenerCuentaSeleccionada();
        
        if (cuentaActiva != null) {
            // La vista delega el retiro y la persistencia al controlador
            boolean exito = controller.retirar(cuentaActiva, monto);
            
            if (exito) {
                javax.swing.JOptionPane.showMessageDialog(this, "Retiro procesado con éxito.");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar en Base de Datos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
            txtMonto.setText("");
            actualizarPantalla();
        }
        } catch (SaldoInsuficienteException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Saldo Insuficiente", javax.swing.JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnRetirarActionPerformed

    private void btnInteresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInteresActionPerformed
        CuentaBancaria cuentaActiva = obtenerCuentaSeleccionada();
        double interesGenerado = cuentaActiva.calcularInteres();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Interés calculado para " + cuentaActiva.getClass().getSimpleName() + 
                "\nMonto Abonado: S/. " + interesGenerado, "Intereses Aplicados", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        actualizarPantalla();
    }//GEN-LAST:event_btnInteresActionPerformed

