    private void inicializarCuentasDelBanco() {
        this.controller = new CuentaBancariaController();

        System.out.println("--- Cargando cuentas mediante el Controlador (MVC) ---");
        listaCuentas = (java.util.ArrayList<model.CuentaBancaria>) controller.listarCuentas();
        System.out.println("--- Cuentas cargadas: " + listaCuentas.size() + " ---");

        // Si por alguna razón la base de datos estuviera vacía, puedes dejar una de respaldo:
        if (listaCuentas.isEmpty()) {
            System.out.println("La base de datos está vacía. Registrando cuentas iniciales de prueba...");
            model.CuentaBancaria c1 = new model.CuentaAhorros("191-456789-0-12", "Juan Carlos Alzamora", 2000.0, "15/01/2026", 0.02, 500.0);
            model.CuentaBancaria c2 = new model.CuentaCorriente("191-987654-3-45", "Ana Maria Lopez", 1500.0, "10/02/2026", 1000.0);
            model.CuentaBancaria c3 = new model.CuentaPlazoFijo("191-112233-4-88", "Pedro Pablo Kuczynski", 10000.0, "01/03/2026", "01/09/2026", 6);

            controller.registrarCuenta(c1);
            controller.registrarCuenta(c2);
            controller.registrarCuenta(c3);

            listaCuentas = (java.util.ArrayList<model.CuentaBancaria>) controller.listarCuentas();
        }
    }

    private model.CuentaBancaria obtenerCuentaSeleccionada() {
        int indiceSeleccionado = cmbCuentas.getSelectedIndex();
        // El ArrayList recupera el elemento usando .get() en lugar de []
        return listaCuentas.get(indiceSeleccionado);
    }

    private void actualizarPantalla() {
        StringBuilder sb = new StringBuilder();
        sb.append("=========================================\n");
        sb.append("         ESTADO DE CUENTAS ACTUALIZADO    \n");
        sb.append("=========================================\n\n");
        
        // Recorremos el ArrayList polimórficamente sin importar cuántas cuentas existan
        for (model.CuentaBancaria cuenta : listaCuentas) {
            sb.append("-> Tipo: ").append(cuenta.getClass().getSimpleName()).append("\n");
            sb.append("   ").append(cuenta.generarEstadoCuenta()).append("\n");
            sb.append("-----------------------------------------------------------------\n");
        }
        
        txtAreaConsola.setText(sb.toString());
    }
    
